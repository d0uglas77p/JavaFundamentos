package poo.encapsulamento.getset;

/*

classe chamada Caneta que utiliza encapsulamento.
Com atributos privados (modelo e ponta) e métodos públicos (getModelo(), setModelo(), getPonta(), setPonta()).
Para acessar e modificar esses atributos de maneira controlada.

O encapsulamento é uma prática importante em programação orientada a objetos.
Para proteger os dados e garantir um acesso seguro.

Na classe Main, exemplificou o uso da classe Caneta ao criar uma nova instância (c1).
Utilizando os métodos setter para definir o modelo e a ponta da caneta.
Além disso, o método getter (getModelo()) para acessar e imprimir o modelo da caneta.

Um ponto importante é que ao utilizar métodos getter e setter, você está seguindo o princípio de encapsulamento.
Permitindo que a classe Caneta tenha controle sobre como seus atributos são acessados e modificados.
Isso promove a segurança e manutenibilidade do código.


// O METODO GET E O SET VAI "PROTEGER" O ATRIBUTO QUE É PRIVADO
// getter = pegar, acessar
// setter = entregar, passar e passar "+1 objeto"

// get é usado para métodos de leitura (obter valores).
// set é usado para métodos de escrita (definir valores).
// Essa prática ajuda a manter o código claro e compreensível.

 */
