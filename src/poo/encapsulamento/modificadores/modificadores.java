package poo.encapsulamento.modificadores;

/*
Os modificadores de acesso public, private e protected são utilizados em Java para controlar a visibilidade de classes, atributos e métodos dentro de uma aplicação.
Eles determinam como esses elementos podem ser acessados por outras partes do código (ou por outras classes) dentro ou fora do mesmo pacote.

Resumo:
public: Acesso aberto a qualquer classe em qualquer lugar.
private: Acesso restrito somente à própria classe onde foi declarado.
protected: Acesso para classes no mesmo pacote ou por subclasses (mesmo que estejam em pacotes diferentes).
 */