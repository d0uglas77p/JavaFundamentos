package estrutura;

/**
 * Classe para documentar
 * @author Douglas
 * @version 1.0
 */
public class documentado {

    /**
     * Método principal que realiza uma conta simples
     */
    public static void main(String[] args) {

        /**
         * @param a Recebe um valor
         */
        int a = 2;
        /**
         * @param b Recebe um valor
         */
        int b = 3;
        /**
         * @param resultado Recebe o resultado final
         */
        int resultado = a+b;

        /**
         * Imprimi o resultado
         */
        System.out.println("A soma de a + b é = " + resultado);
    }
}
